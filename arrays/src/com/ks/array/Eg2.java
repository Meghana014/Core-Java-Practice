	public static void main(String[] args) {
		
		int []a=new int[4];
		System.out.println("default value at index 0: "+a[0]);
		System.out.println("default value at index 1: "+a[1]);
		System.out.println("default value at index 2: "+a[2]);

		int []i=new int[3];
		i[0]=21;
		i[1]=22;
		i[2]=54;
		System.out.println("integer value at index position 0:"+i[0]);
		System.out.println("integer value at index position 1:"+i[1]);
		System.out.println("integer value at index position 2:"+i[2]);
		
		String []s=new String[3];
	    s[0]="meghana";
	    s[1]="how are you";
	    System.out.println("string at index position 0: "+s[0]);
	    System.out.println("string at index position 1: "+s[1]);
		
		

	}

