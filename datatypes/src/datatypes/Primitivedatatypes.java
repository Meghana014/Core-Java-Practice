package datatypes;

public class Primitivedatatypes {
	public static void main(String[] args) {
		int a=1;
		System.out.println(a);
		char ch='a';
		System.out.println(ch);
		double d=1.234;
		System.out.println(d);
		long l= 245771235L;
		System.out.println(l);
		float f= 1.123f;
		System.out.println(f);
		
		
	}

}
