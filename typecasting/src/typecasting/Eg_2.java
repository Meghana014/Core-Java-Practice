package typecasting;
//explicit type casting
public class Eg_2 {
	public static void main(String[] args) {
		
		double d=20.0;
		int i=(int)d;
		System.out.println(i);
		
		float f=23.4f;
		byte b=(byte)f;
		System.out.println(b);
		
		long l=234554676l;
		int i1=(int)l;
		System.out.println(i1);
		
		double d1=245.8907486;
		float f1=(float)d1;
		System.out.println(f1);
		
		int i2=876;
		byte b1=(byte)i2;
		System.out.println(b1);
		
		
		
		
		
		
		
		
		
	}

}
